const carList = document.querySelector(".js-list");
const formEl = document.querySelector(".js-search-form");
formEl.addEventListener("submit", onSearch);

const markup = cars
  .map(
    ({ id, model, type, price, img }) =>
      `<li data-id='${id}'>
        <img src="${img}" alt="${model}" />
        <h2>${model}</h2>
        <h3>${type}</h3>
        <p>${price}</p>
      </li>`
  )
  .join("");

carList.insertAdjacentHTML("beforeend", markup);

function onSearch(event) {
  event.preventDefault();
  const form = event.currentTarget;
  const { select, query } = form.elements;
  const searchCars = cars.filter(
    (item) =>
      item[select.value].toLowerCase() === query.value.trim().toLowerCase()
  );
  console.log(searchCars);
}
